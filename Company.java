package com.company;

public class Company {
    String name;
    String phone;
    String fax;

    public void setName(String name)
    {
        this.name = name;
    }
    public String getName()
    {
        return this.name;
    }

    public void setPhone(String temp)
    {
        this.phone = temp;
    }
    public String getPhone()
    {
        return this.phone;
    }

    public void setFax(String temp)
    {
        this.fax = temp;
    }
    public String getFax()
    {
        return this.fax;
    }
}
